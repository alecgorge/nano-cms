<h2>About NanoCMS v0.1</h2>
<p>
	Nano CMS is the tinyest CMS you can find around or the tinyest that i could have made :)<br>
	The user interface and the functionality are very very simple and easy to use.<br><br>
</p>
<p>
	Through this CMS you can create only pages without actually touching editors or anything.<br>
	You can manage pages which appear in sidebar and some simple pages which you might want to link saperately<br>
	nano cms is simple and neat enough to manage websites with static content through an easy to use interface.<br>
	If you dont like the design it is very easy to integrate new designs also...
</p>

<h2>How to create pages</h2>
<p>
	Creating pages is very simple..just follow these steps
	<ul>
		<li>Click on 'Add page'</li>
		<li>Enter the title of page</li>
		<li>Select weather it should be displayed in sidebar or not</li>
		<li>Enter the content</li>
		<li>Click Submit and viola the page is created</li>
	</ul>
</p>
