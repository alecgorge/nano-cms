<h2>How to create pages?</h2>
<ul>
		<li>Click on 'Add page'</li>
		<li>Enter the title of page</li>
		<li>Select weather it should be displayed in sidebar or not</li>
		<li>Enter the content</li>
		<li>Click Submit and viola the page is created</li>
</ul>
<h2>How to Manage Content Areas ?</h2>
<ul>
  <li>The content area editing options are present in the page &quot;Show Content   Areas&quot;. </li>
  <li>All the content area blocks in the template/design are shown in page. </li>
  <li>Each area will have its own options </li>
  <li>If you feel the box is too small you can use <strong>+, -   &lt;, &gt;</strong> to increase the size of the input box</li>
</ul>
