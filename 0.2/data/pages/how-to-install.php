<h2>How to install NanoCMS</h2>
<ul>
 <li> Simply copy the files into the  location where you want it to work.</li>
 <li>Thats it NanoCMS is installed!!!!!.</li>
</ul>
<h2>Configuring NanoCMS</h2>
<ul>
  <li> give <b>chmod 777</b> permissions for <strong>&quot;data/pages&quot;</strong> and <strong>&quot;date/areas&quot;</strong> folder. ( these permissions are required as files are written to these folders ) </li>
  <li>Open the page <strong>&quot;settings.php&quot;</strong> in notepad or any other text editor</li>
  <li>Edit the <strong>admin login</strong> and <strong>admin password</strong>, which are by default '<strong>admin</strong>' and '<strong>demo</strong>' respectively.</li>
  <li>Set the main page of your NanoCMS site which is defaulted to 'index.php' ( recomended )</li>
  <li>Login into your admin panel by opening the page <strong>'nanoadmin.php'</strong></li>
</ul>
