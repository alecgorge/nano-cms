<h2>Instant Helpers</h2>

<h2 class="noborder">&raquo; What are Tweakers</h2>
<ul>
  <li>Tweakers are plugin-like tools which can be used to tweak the NanoCMS without having to modify the core files.<br />
    New functionalities and facilities can easily be added to NanoCMS.</li>
</ul>
<h2 class="noborder">&raquo; How to create new pages?</h2>
<ul>
		<li>Click on 'Add page'</li>
		<li>Enter the title of page</li>
		<li>Select where on the Home Page a link will be inserted to the new page.</li>
		<li>Enter the page content</li>
		<li>Click Submit and the page is created</li>
</ul>
<h2 class="noborder">&raquo; How to Manage Content Areas ?</h2>
<ul>
  <li>The content area editing options are present in the page &quot;Show Content   Areas&quot;. </li>
  <li>All the content area blocks in the template/design are shown in page. </li>
  <li>Each area will have its own options </li>
</ul>


