<h2>About NanoCMS (v0.4-final)</h2>
<p>Nano CMS is the tiniest CMS you can find around. It is fully file-based and does not use a database, has extremely easy, simple, and friendly user interface.</p>
<p>Pages can be created with nanoCMS without having to change any code, and links to the new pages are generated and inserted automatically.
Customizing the design/look and feel is very simple too, making it ideal for newbies.</p>

<p> You can visit the website (<a href="http://nanocms.in">nanocms.in</a>) or the forums (<a href="http://nanocms.in/forums">nanocms.in/forums</a>) for more info &amp; discussion. Visit the blog (<a href="#">nanocms.in/blog</a>) for latest news and updates.</p>
