<h2>About NanoCMS (v0.31)</h2>
<p> Nano CMS is the tinyest CMS you can find around...:)<br />
The user interface and the functionality are very very simple and easy to use.<br />
NanoCMS is filebased, so no database blah,. Easy to install, easy to use. <br />
<br />
Through this CMS you can create pages without actually touching any code !.<br />
You can manage website pages very easily and the links in the sidebar are generated automatically...</p>
