<H2>How to install NanoCMS</H2>
<UL>
  <LI>Simply Extract and copy the files into the location where you want it to work.
  <LI>Thats it NanoCMS is installed!!!!!.</LI>
</UL>
<H2>Configuring NanoCMS</H2>
<UL>
  <LI>give <strong>chmod 777</strong> permissions for <STRONG>"data/pages"</STRONG> and <STRONG>"date/areas"</STRONG> folder. ( these permissions are required as files   are written to these folders )
  <LI>Open the page <STRONG>"data/config.php"</STRONG> in notepad or any other text   editor
  <LI>Edit the config according to your choice.
  <LI>Thats it, Login into your admin panel by opening the page <STRONG>'nanoadmin.php'</STRONG>
</UL>
